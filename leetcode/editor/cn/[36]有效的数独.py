class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        # Âà§Êñ≠Ë°å
        for i in range(9):
            st = [False] * 10
            for j in range(9):
                if board[i][j] != '.':
                    t = int(board[i][j])
                    if st[t]:
                        return False
                    st[t] = True

        # Âà§Êñ≠Âàó
        for j in range(9):
            st = [False] * 10
            for i in range(9):
                if board[i][j] != '.':
                    t = int(board[i][j])
                    if st[t]:
                        return False
                    st[t] = True

        # Âà§Êñ≠‰πùÂÆ´Ê†º
        for i in range(0, 9, 3):
            for j in range(0, 9, 3):
                st = [False] * 10
                for x in range(0, 3):
                    for y in range(0, 3):
                        if board[i + x][j + y] != '.':
                            t = int(board[i + x][j + y])
                            if st[t]:
                                return False
                            st[t] = True

        return True

